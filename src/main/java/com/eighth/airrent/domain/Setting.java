public class Setting {

    private String setttingId;
    private String indexMessage;
    private String choice;
    private String feeInfo;
    private String content;
    private String cycle;
    private String time;

    public String getSetttingId() {
        return setttingId;
    }

    public void setSetttingId(String setttingId) {
        this.setttingId = setttingId;
    }

    public String getIndexMessage() {
        return indexMessage;
    }

    public void setIndexMessage(String indexMessage) {
        this.indexMessage = indexMessage;
    }

    public String getChoice() {
        return choice;
    }

    public void setChoice(String choice) {
        this.choice = choice;
    }

    public String getFeeInfo() {
        return feeInfo;
    }

    public void setFeeInfo(String feeInfo) {
        this.feeInfo = feeInfo;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCycle() {
        return cycle;
    }

    public void setCycle(String cycle) {
        this.cycle = cycle;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
